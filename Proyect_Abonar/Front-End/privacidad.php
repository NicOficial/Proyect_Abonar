          <h3>Datos recolectados</h3>
          <p class="recolectados">
            A través de Abonar recabamos determinados datos, incluidos datos de
            carácter personal. La información de carácter personal es
            información que puede identificarlo a Ud. Para Abonar, su privacidad
            y la seguridad de su información son muy importantes. Por ello, solo
            le solicitaremos los datos estrictamente necesarios para que Ud.
            pueda utilizar los servicios que Abonar le ofrece. A tal fin, Abonar
            podrá solicitar y tratar los datos que a continuación se enumeran,
            sin que la siguiente sea una lista taxativa: Datos de contacto
            (tales como nombre y apellido, ; domicilios, teléfono, correo
            electrónico); DNI; Datos demográficos (tales como nacionalidad;
            fecha y lugar de nacimiento, edad, sexo); una fotografía de su DNI;
            Recopilamos dichos datos al momento de su registro, y también
            recopilamos información personal acerca de su persona cuando Ud. nos
            brinda información directamente a nosotros o cuando interactúa con
            nosotros a través de Abonar.
          </p>
          <br />
          <h3>Tratamiento de los datos personales</h3>
          <p class="personales">
            Los datos que recolectamos pueden ser utilizados para los siguientes
            fines: comunicarnos con Ud.; enviarle materiales promocionales y
            otras comunicaciones; responder a sus consultas; personalizar las
            preferencias y visitas a Abonar de nuestros usuarios y ofrecer
            contenidos adaptados a sus intereses y a la forma en que navegan e
            interactúan con Abonar; operar, evaluar y mejorar nuestros productos
            y servicios; recabar la documentación respaldatoria y demás datos
            que justifiquen cualquier tipo de operación o transacción; y
            cumplir, prevenir actividades ilícitas como lavado de dinero,
            evasión de impuestos y fraudes y hacer cumplir los requisitos
            legales aplicables, las normas correspondientes del sector, las
            obligaciones contractuales y nuestras políticas. Para el caso de que
            vayamos a usar sus datos personales de forma distinta a la descrita
            anteriormente, se lo comunicaremos de forma específica en el momento
            de recolectarlos. A través de Abonar, podemos además usar servicios
            analíticos de terceros. Los proveedores de servicios de análisis de
            webs que administran dichos servicios utilizan tecnologías tales
            como cookies, registros de servidores de red y balizas web que nos
            ayudan a analizar el uso que los usuarios hacen de Abonar. La
            información recogida por estos medios (incluida la dirección IP)
            puede ponerse en conocimiento de los proveedores de tales servicios
            analíticos, así como de otros terceros que vayan a hacer un uso de
            la información recabada para, entre otros fines, evaluar el uso de
            Abonar.
          </p>
